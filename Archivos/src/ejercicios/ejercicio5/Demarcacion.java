package ejercicios.ejercicio5;



public class Demarcacion {

	private String demarcaciones;
	private String codigo;
	public Demarcacion(String demarcacion, String codigo) {
		super();
		this.demarcaciones = demarcacion;
		this.codigo = codigo;
	}
	public String getDemarcaciones() {
		return demarcaciones;
	}
	public void setDemarcaciones(String demarcacion) {
		this.demarcaciones = demarcacion;
	}
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	
	
}
