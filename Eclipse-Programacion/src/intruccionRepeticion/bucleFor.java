package intruccionRepeticion;

public class bucleFor {

	public static void main(String[] args) {
		/*
		 * Prueba del blucle for
		 * 
		 * for ( i = 0 ; condiciÃ³n ; incremento)
		 */
		
		for (int i = 500; i>0; i/=2) {
			System.out.println(i + "Hola");
		}
		
		
		//Se puede incrementar con decimales
		for (double i = 1; i<10; i+=0.5) {
			System.out.println(i + "Hola");
		}

		
		
		

	}

}
